    // ================================================================
    // イベントポイント配置
    // ================================================================
    public static void BuildEventPoints()
    {
        foreach (var p in _points)
            if (p != null) Object.Destroy(p.gameObject);
        _points.Clear();

        // ---- 章のストーリーポイント ----
        (string id, string label, Vector3 pos, bool auto)? sp = StoryManager.Chapter switch
        {
            0  => ("opening",      "──",                       new Vector3(0, 0, -9),     true),
            1  => ("niyo",         "気配を調べる",             new Vector3(-18, 0, -18),  false),
            2  => ("androidFight", "異変を調べる",             new Vector3(0, 0, 13),     false),
            3  => ("misa",         "倒れた機体を調べる",       new Vector3(0, 0, 13),     false),
            4  => ("rei",          "巫女に話しかける",         new Vector3(24, 1.1f, 4),  false),
            5  => ("runaFight",    "視線の主を追う",           new Vector3(-20, 0, 3),    false),
            6  => ("runaPochi",    "ルナと合流する",           new Vector3(-18, 0, -18),  false),
            7  => ("tragedy",      "広場の様子を見る",         new Vector3(0, 0, -9),     false),
            8  => ("ryuFight",     "時計塔に入る",             new Vector3(10, 0, 8),     false),
            9  => ("rescue",       "リュウに呼びかける",       new Vector3(0, 0, -9),     false),
            10 => ("revival",      "時間逆行の儀を行う",       new Vector3(10, 0, 8),     false),
            11 => ("finalFight",   "決戦の刻",                 new Vector3(0, 0, -9),     false),
            _  => ("postEnd",      "あの日を思い出す",         new Vector3(0, 0, -9),     false),
        };

        if (sp.HasValue)
            _points.Add(FieldEventPoint.Create(sp.Value.id, sp.Value.label,
                EventPointKind.Story, sp.Value.pos, sp.Value.auto));

        // ---- ミニゲーム（常設） ----
        _points.Add(FieldEventPoint.Create("mg_bubble", "シャボン玉あそび",
            EventPointKind.Minigame, new Vector3(8, 0, -30)));
        _points.Add(FieldEventPoint.Create("mg_ice", "アイス早食い競争",
            EventPointKind.Minigame, new Vector3(-24, 0, 2)));
        _points.Add(FieldEventPoint.Create("mg_hanabi", "花火打ち上げ",
            EventPointKind.Minigame, new Vector3(-13, 0, -15)));

        // ---- 住人（常設・フレーバー会話） ----
        _points.Add(FieldEventPoint.Create("npc_shop", "店主と話す",
            EventPointKind.Npc, new Vector3(-19, 0, 2)));
        _points.Add(FieldEventPoint.Create("npc_plaza", "住人と話す",
            EventPointKind.Npc, new Vector3(5, 0, -10)));
        _points.Add(FieldEventPoint.Create("npc_school", "生徒と話す",
            EventPointKind.Npc, new Vector3(-2, 0, 11)));
    }

