    // ================================================================
    // ヘルパー
    // ================================================================
    private static List<DialogueLine> L(params (string speaker, string text)[] lines)
    {
        var list = new List<DialogueLine>();
        foreach (var (s, t) in lines) list.Add(new DialogueLine(s, t));
        return list;
    }

    private static void Join(string id)
    {
        StoryManager.AddPartyMember(id);
        _player?.RebuildFollowers();
    }

    private static void Leave(string id)
    {
        StoryManager.RemovePartyMember(id);
        _player?.RebuildFollowers();
    }

    private static void Advance()
    {
        StoryManager.AdvanceChapter();
        BuildEventPoints();
    }
